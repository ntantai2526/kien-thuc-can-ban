//2. Random số lớn hơn 1
let randomValue1 = Math.random()*10;
console.log(`Số ngẫu nhiên từ 0 - 1: ${randomValue1}`);

//3. Random số nguyên
let randomValue2 = parseInt(Math.random()*10);
console.log(`Số ngẫu nhiên từ 0 - 1: ${randomValue2}`);

//chạy từ 0 đến 30
let randomValue3 = Math.random()*31;
